
/*Primitive Datatype 
 Number/Boolean/Null/Undefined/String/Symbols
*/
var a=25;
console.log(typeof a);

var a="Ramya"
console.log(typeof a);

var a=12.256;
console.log(typeof a);

var ac='a';
console.log(typeof ac);

a=true;
console.log(typeof a);

/* Non Primitive Datatype 
Array/ Object Literals/ Date
*/

